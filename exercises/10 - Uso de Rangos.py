#Ejercicio 1 - Iterar un rango de 0 a 10, imprimiendo solo los números divisibles entre 3
print('Ejercicio 1: Iteración de 1 a 10')
for numero in range(11):
    if numero % 3 == 0:
        print(numero)

#Ejercicio 2 - Crea un rango de 2 a 6 e imprimelo
print('Ejercicio 2: Rango de 2 a 6')
for numero in range (2,7):
    print(numero)

#Ejercicio 3 - Crea un rango de 3 a 10, con incremento de 2 en 2
print('Ejercicio 3: Rango de 3 a 11, con salto de 2')
for numero in range (3,11,2):
    print(numero)